
public class Items {

	private String itemName;
	private double rank;
	
	public Items(){
		itemName=null;
		rank=1.5;
	}

	/**
	 * @return the itemName
	 */
	public String getItemName() {
		return itemName;
	}

	/**
	 * @param itemName the itemName to set
	 */
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}

	/**
	 * @return the rank
	 */
	public double getRank() {
		return rank;
	}


	
	
	
}
